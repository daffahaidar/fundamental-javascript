// logical operator
let a = true;
let b = false;
let result;

// logical AND (will return true if both operands are true)
result = a && b;
console.log(result);

// logical OR (will return true if one of the operands is true)
result = a || b;
console.log(result);

// logical NOT (will return false if the operand is true)
result = !a;
console.log(result);

// logical XOR (will return true if only one of the operands is true)
result = a ^ b;
console.log(result);

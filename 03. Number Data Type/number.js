// integer number
console.log(100);

// floating point number
console.log(100.5);

// number notation
// hexadecimal: add 0x before the number
console.log(0xff);

// binary: add 0b before the number
console.log(0b1010);

// octal: add 0o before the number
console.log(0o744);

// exponential notation
console.log(1e6);

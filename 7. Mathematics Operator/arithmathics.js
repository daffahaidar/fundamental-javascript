let number1 = 10;
let number2 = 20;

// addition
let result = number1 + number2;
console.log(result);

// subtraction
result = number2 - number1;
console.log(result);

// multiplication
result = number1 * number2;
console.log(result);

// division
result = number2 / number1;
console.log(result);

// modulus
result = number2 % number1;
console.log(result);

// exponentiation
result = result + 1;
console.log(result);

// or

result += 1;
console.log(result);

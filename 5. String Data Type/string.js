// string using ""
console.log("Hello World");
// string using ''
console.log("Hello World");
// string using ``
console.log(`Hello World`);

// string concatenation
console.log("Name: " + "Daffa Haidar Nabil Zufar");
console.log(
  "Name: " + "Daffa" + " " + "Haidar" + " " + "Nabil" + " " + "Zufar"
);

// escape character
// \n new line
console.log("Hello\nWorld");
// \t tab
console.log("Hello\tWorld");
// \b backspace
console.log("Hello\bWorld");
// \r carriage return
console.log("Hello\rWorld");
// \f form feed
console.log("Hello\fWorld");
// \v vertical tab
console.log("Hello\vWorld");
// \0 null
console.log("Hello\0World");
// \ddd octal
console.log("Hello\077World");
// \xdd hexadecimal
console.log("Hello\x41World");
// \uddd unicode
console.log("Hello\u0041World");
// \' single quote
console.log("Hello'World'");
// \" double quote
console.log('Hello"World"');
// \\ backslash
console.log("Hello\\World\\");

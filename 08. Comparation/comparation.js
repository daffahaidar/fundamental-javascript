let a = 2;
let b = 3;
let result;

// comparison
result = a > b;
console.log(result);

result = a < b;
console.log(result);

result = a >= b;
console.log(result);

result = a <= b;
console.log(result);

// comparation with different data types
result = 9 == "9";
console.log(result);

result = 9 != "9";
console.log(result);

// comparation with same data types
result = 9 === "9";
console.log(result);

result = 9 !== "9";
console.log(result);

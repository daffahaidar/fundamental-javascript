// variable using the var keyword

// initialize variable
var firstName;
myName = "John";

// we can reassign the value of a variable
myName = "Jane";

// initialize variable with value
var lastName = "Iskandar";
console.log(firstName + " " + lastName);

// variable using the let keyword
// initialize variable
let firstName = "John";
let lastName = "Iskandar";

// we can reassign the value of a variable
firstName = "Jane";

console.log(firstName + " " + lastName);

// variable using the const keyword
// initialize variable
const firstName = "John";
const lastName = "Iskandar";

// for const variable, we can't reassign the value
firstName = "Jane"; // error
console.log(firstName + " " + lastName);
